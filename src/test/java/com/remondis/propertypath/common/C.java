package com.remondis.propertypath.common;

public class C {

  private String c;

  public C() {
    super();
  }

  public C(String c) {
    super();
    this.c = c;
  }

  public String getC() {
    return c;
  }

  public void setC(String c) {
    this.c = c;
  }

  @Override
  public String toString() {
    return "C [c=" + c + "]";
  }

}
